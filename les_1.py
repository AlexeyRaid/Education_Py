# Завдання 4
# Ознайомтеся за допомогою документації з класами namedtuple та deque модуля collections. Створіть фабрику іменованих кортежів оцінок для учнів однієї групи з предметів: алгебра, геометрія, історія, інформатика, географія. Вивести дані на екран.
from collections import namedtuple, deque

Score = namedtuple('Score', ['name', 'algebra', 'geometry', 'history', 'informatics', 'geography'])
number_students = int(input("Введіть кількість учнів у класі: "))

students_scores = deque()

for i in range(1, number_students + 1):
    name = input(f"Введіть ПІБ учня {i}: ")
    algebra = int(input(f"Введіть оцінку з алгебри для учня {name}: "))
    geometry = int(input(f"Введіть оцінку з геометрії для учня {name}: "))
    history = int(input(f"Введіть оцінку з історії для учня {name}: "))
    informatics = int(input(f"Введіть оцінку з інформатики для учня {name}: "))
    geography = int(input(f"Введіть оцінку з географії для учня {name}: "))

    st_score = Score(name, algebra, geometry, history, informatics, geography)
    students_scores.append(st_score)  # Додаємо створений кортеж до черги

for score in students_scores:
    print(score)

