# Завдання 2
# Напишіть програму, в якій користувач вводить із клавіатури діапазон чисел (в діапазоні має бути не менше 5 чисел). Вивести на екран суму другого, передостаннього, а також середнього арифметичного значення даної послідовності.

us_anwer = input("Введіть будь ласка не менше 5 чисел через пробіл: ")
ans = us_anwer.split(" ")
if len(ans) < 5:
    print(f"Ви ввели {len(ans)} чисел, а потрібно було 5. Це неприпустимо")
    exit()
for i in range(len(ans)):
    if not ans[i].isdigit() or not ans[i].isdecimal():
        print(f"Як мінімум, одне з введених  вами значень не є числом")
        exit()

# Перетворюєм дані кортежу в числовий формат, щоб не робити це при кожній дії
ans_numbers = [float(num) for num in ans]

sum_res = ans_numbers[1] + ans_numbers[-2]
print(f"Сума другого ({ans_numbers[1]}) та передостанього ({ans_numbers[-2]}) числа дорівнює {sum_res}")

avrg = sum(ans_numbers) / len(ans_numbers)
print(f"Середнє арифметичне для послідовності {ans_numbers} буде складати {avrg}")

